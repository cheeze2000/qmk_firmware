bool oled_task_user(void) {
    oled_clear();

    if (is_keyboard_master()) {
        switch (get_highest_layer(layer_state)) {
            case 0:
                oled_write_raw_P(layer_0, sizeof(layer_0));
                break;
            case 1:
                oled_write_raw_P(layer_1, sizeof(layer_1));
                break;
            case 2:
                oled_write_raw_P(layer_2, sizeof(layer_2));
                break;
            case 3:
                oled_write_raw_P(layer_3, sizeof(layer_3));
                break;
        }

        oled_set_cursor(0, 3);
        if (get_mods() & MOD_MASK_CTRL) {
            oled_write_raw_P(ctrl_on, sizeof(ctrl_on));
        } else {
            oled_write_raw_P(ctrl_off, sizeof(ctrl_off));
        }

        oled_set_cursor(0, 6);
        if (get_mods() & MOD_MASK_ALT) {
            oled_write_raw_P(alt_on, sizeof(alt_on));
        } else {
            oled_write_raw_P(alt_off, sizeof(alt_off));
        }

        oled_set_cursor(0, 9);
        if ((get_mods() & MOD_MASK_SHIFT) | (get_oneshot_mods() & MOD_MASK_SHIFT)) {
            oled_write_raw_P(shift_on, sizeof(shift_on));
        } else {
            oled_write_raw_P(shift_off, sizeof(shift_off));
        }
    } else {
        char apm[6];
        sprintf(apm, "%5d", get_current_wpm());

        oled_write_ln_P(PSTR("\n\n\n\nAPM\n"), false);
        oled_write_ln_P(apm, false);
    }

    return false;
}

